print("Qual é a média das idades?")
idade1 = float(input("Primeira idade: "))
idade2 = float(input("Segunda idade: "))
idade3 = float(input("Terceira idade: "))
idade4 = float(input("Quarta idade: "))
idade5 = float(input("Quinta idade: "))

media = (idade1 + idade2 + idade3 + idade4 + idade5) / 5

print("A média das idades é de: ", media)
